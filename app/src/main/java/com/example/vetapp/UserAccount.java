package com.example.vetapp;

public class UserAccount {
    public String firstName, lastName, phone, email, username, password;

    public UserAccount(String firstName, String lastName, String phone, String email, String username, String password) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.phone = phone;
        this.email = email;
        this.username = username;
        this.password = password;
    }
}

